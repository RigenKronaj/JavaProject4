    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = jTextField1.getText();
        String sqanswer = jTextField2.getText();
        String password = jPasswordField1.getText();
        String password2 = jPasswordField2.getText();
        
        if (username.trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Username nuk duhet te jete bosh");
        }
        else if (password.trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Password nuk duhet te jete bosh");
        }
        else if (sqanswer.trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Security answer nuk duhet te jete bosh");
        }
        else if (!password.equals(password2)){
            JOptionPane.showMessageDialog(this, "Passwordet nuk jane te njejte");
        }

        else{
            try{
                Class.forName("com.mysql.cj.jdbc.Driver");
                conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/bibliotekagui", "root", "admin");
                PreparedStatement pst = conn.prepareStatement("select * from users");
                rs = pst.executeQuery();
                if(rs.next()){
                    pst.executeUpdate("Insert into USERS (username, password, sqanswer) values ('"+username+"','"+password+"', '"+sqanswer+"')");
                    JOptionPane.showMessageDialog(this, "Perdoruesi u shtua ne database");
                    LoginFrame frame = new LoginFrame();
                    frame.setVisible(true);
                    setVisible(false);
                }
                
                else{
                    JOptionPane.showMessageDialog(this, "Gabim ne sintakse");
                }
                
                LoginFrame frame = new LoginFrame();
                frame.setVisible(true);
                setVisible(false);
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(RegisterFrame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(RegisterFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

